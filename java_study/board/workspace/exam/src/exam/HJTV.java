package exam;

public class HJTV {
	public void powerOn() {
		System.out.println("TV가 켜졌습니다");
	}
	public void powerOff() {
		System.out.println("TV가 꺼졌습니다");
	}
	public void volumeDown() {
		System.out.println("TV의 볼륨을 줄였습니다");
	}
	public void volumeUp() {
		System.out.println("TV의 볼륨을 높였습니다");
	}
}
